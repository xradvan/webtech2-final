                <h1>Používateľská príručka</h1>
                <p>Táto webová aplikácia je súčasťou semestrálneho zadania predmetu Webové technológie 2 študentov FEI STU API. Jej zaznamenávať jednotlivé trasy tréningu používateľov, zobrazovanie výkonov, odoberanie aktualít a definovanie nových trás, ktoré používateľ odbehne.
                </p>
                <p>Registrácia je prístupná pre každého, kto chce mať prehľad o svojich tréningoch. Aplikácia umožňuje administrátorovi spravovať trasy, určovať práva používateľov, vytvárať tímy pre štafetový mód a pridávať aktuality. Používateľ si môže vybrať z viacerých typov módov trás a to:</p>
            <ul>
                <li>Štafetový - Na jednej trase môžu byť priradené viaceré tímy, výkony a trasy vidia všetci používatelia webovej aplikácie</li>
                <li>Verejný - Výkony a trasy vidia všetci používatelia webovej aplikácie</li>
                <li>Privátny - Výkony a trasy vidí len práve prihlásený používateľ a administrátor webovej aplikácie</li>
            </ul>
            <h4><strong>Úvodná stránka</strong></h4>
            <p>Na úvodnej stránke si môže neprihlásený používateľ zobraziť adresy alebo školy všetkých zaregistrovaných používateľov prostredníctvom mapy. Stránka slúži aj na prihlásenie alebo registráciu, na ktorú sa dá dostať z pravého rohu navigačného panela.</p>
            <img src="img/prologue.png" width="100%">
            <h4><strong>Domovská obrazovka</strong></h4>
            <ul>
                <li>Administrátor - Má k dispozícii definovanie trasy, pridanie tímu pre štafetový mód, zobrazenie vybranej trasy ako aj zmenu aktívnej trasy. Pri výbere trasy sa zobrazí graf výkonov jednotlivých používateľov, respektíve tímov. Administrátor vidí trasy vo verejnom, privátnom a štafetovom móde</li>
                <li>Bežný používateľ - Má k dispozícii vytvorenie trasy v privátnom móde, prezeranie svojích trás v privátnom móde a v štafetovom a taktiež vidí všetky trasy vo verejnom móde. Používateľ si môže pridať prostredníctvom formuláru tréning na vybranej trase. Svoje trasy môže zaktivovať alebo zdeaktivovať, pričom je vždy len jedna aktívna</li>
            </ul>
            <img src="img/cesty.png" width="100%">
            <h4><strong>Aktuality</strong></h4>
             <ul>
                <li>Administrátor - vidí všetky aktuality, pričom môže pridať novú aktualitu, či ju zmeniť alebo zmazať. Pri pridaní trasy sa odošle prostredníctvom e-mailu upozornenie všetkým používateľom, ktorí sú odberateľmi aktualít.</li>
                <li>Bežný používateľ - Vidí všetky aktuality, ktoré pridal administrátor</li>
            </ul>
            <h4><strong>Osobné výkony</strong></h4>
            <p>Používateľ má k dispozícii prehľadnú tabuľku všetkých svojích výkonov a taktiež si ju môže stiahnuť vo forme PDF.</li>
            </p>
            <h4><strong>Používatelia</strong></h4>
            <p>
                Stránka je viditeľná len pre prihláseného administrátora. Poskytuje zoznam všetkých registrovaných používateľom, pričom im môže nastaviť práva a zobraziť ich osobné výkony. Na tejto stránke vie taktiež hromadne zaregistrovať nových používateľov prostredníctvom CSV súboru.
            </p>
